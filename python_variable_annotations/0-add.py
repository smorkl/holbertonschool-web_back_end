#!/usr/bin/env python3

"""
This module provides basic arithmetic operations.

Functions:
    add(a, b): Adds two floating-point numbers.
"""


def add(a: float, b: float) -> float:
    """Adds two floating-point numbers.

    Args:
        a: The first number.
        b: The second number.

    Returns:
        The sum of a and b.
    """
    return a + b
